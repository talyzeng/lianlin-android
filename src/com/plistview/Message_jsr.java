package com.plistview;

public class Message_jsr {
	private String name;

	private String id;
	private String url;
	
	
	public String getname() {
		return name;
	}
	public void setname(String a) {
		this.name = a;
	}
	
	
	
	public String getId() {
		return id;
	}
	public void setId(String a) {
		this.id = a;
	}
	
	public String getUrl() {
		return url;
	}
	public void setUrl(String a) {
		this.url = a;
	}

}
