package com.plistview;

public class Message_timeline {
	private String yearmonth;
	private String day ;
	private String zhou;
	private String content;
	private String id;
	private String url;
	
	public String getyearmonth() {
		return yearmonth;
	}
	public void setyearmonth(String a) {
		this.yearmonth = a;
	}
	public String getday() {
		return day;
	}
	public void setday(String a) {
		this.day = a;
	}
	public String getzhou() {
		return zhou;
	}
	public void setzhou(String a) {
		this.zhou = a;
	}
	
	
	public String getId() {
		return id;
	}
	public void setId(String a) {
		this.id = a;
	}
	public String getcontent() {
		return content;
	}
	public void setcontent(String a) {
		this.content = a;
	}
	
	public String getUrl() {
		return url;
	}
	public void setUrl(String a) {
		this.url = a;
	}

}
