package com.plistview;

public class Message_type {
	private String title;
	private String id;
	private String url;
	
	
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String a) {
		this.title = a;
	}
	
	
	
	public String getId() {
		return id;
	}
	public void setId(String a) {
		this.id = a;
	}
	
	
	public String getUrl() {
		return url;
	}
	public void setUrl(String a) {
		this.url = a;
	}

}
